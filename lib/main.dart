import 'package:BitirmeProjem/helper/authenticate.dart';
import 'package:BitirmeProjem/helper/helperfunctions.dart';
import 'package:BitirmeProjem/views/chatrooms.dart';
import 'package:flutter/material.dart';

void main() {
  runApp(MyApp());
}

class MyApp extends StatefulWidget {
  // This widget is the root of your application.
  @override
  _MyAppState createState() => _MyAppState();
}

class _MyAppState extends State<MyApp> {
  bool userIsLoggedIn;

  @override
  void initState() {
    getLoggedInState();
    super.initState();
  }

  getLoggedInState() async {
    await HelperFunctions.getUserLoggedInSharedPreference().then((value) {
      setState(() {
        userIsLoggedIn = value;
      });
    });
  }

  @override
  Widget build(BuildContext context) {
    return MaterialApp(
      title: 'BitirmeProjem',
      debugShowCheckedModeBanner: false,
      theme: ThemeData(
        primaryColor: Color(0xff07400c),
        scaffoldBackgroundColor: Color(0xff1b6c22),
        accentColor: Color(0xff8ac14a),
        primarySwatch: Colors.lightGreen,
        fontFamily: "OverpassRegular",
        visualDensity: VisualDensity.adaptivePlatformDensity,
      ),
      home: userIsLoggedIn != null
          ? userIsLoggedIn
              ? ChatRoom()
              : Authenticate()
          : Container(
              child: Center(
                child: Authenticate(),
              ),
            ),
    );
  }
}
